const gallery = document.querySelector("#gallery");
let container = document.createElement("div");
container.setAttribute("class", "container");
let count = 0;

for (let data of datas) {
  count++;
  const card = document.createElement("div");
  card.setAttribute("class", "card");

  const cardImg = document.createElement("div");
  cardImg.setAttribute("class", "card-img");

  const img = document.createElement("img");
  img.setAttribute("src", data.img);
  cardImg.appendChild(img);

  const cardContent = document.createElement("div");
  cardContent.setAttribute("class", "card-content");

  const h3 = document.createElement("h3");
  h3.innerText = data.nama;
  const p1 = document.createElement("p");
  p1.innerHTML = `<span>Tempat lahir:</span> ${data.tempatLahir}`;
  const p2 = document.createElement("p");
  p2.innerHTML = `<span>Tanggal lahir:</span> ${data.tanggalLahir}`;
  const p3 = document.createElement("p");
  p3.innerHTML = `<span>Meninggal:</span> ${data.meninggal}`;
  const p4 = document.createElement("p");
  p4.innerText = data.desc;

  cardContent.appendChild(h3);
  cardContent.appendChild(p1);
  cardContent.appendChild(p2);
  cardContent.appendChild(p3);
  cardContent.appendChild(p4);

  card.appendChild(cardImg);
  card.appendChild(cardContent);

  container.appendChild(card);

  if (count % 3 === 0) {
    gallery.appendChild(container);
    container = document.createElement("div");
    container.setAttribute("class", "container");
  }
}

gallery.appendChild(container);
